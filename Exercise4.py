grade = int(input("Enter your Score (0-100): "))

if grade <=60:
    print("You got an F!")
elif grade <=69:
    print("You got a D!")
elif grade <=80:
    print("You got a C!")
elif grade <=90:
    print("You got a B!")
elif grade <=100:
    print("You got a A!")
else:
    print("Let me know your score.")


